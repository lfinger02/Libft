#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *last;

	last = NULL;
	while (1)
	{
		if (*s == c)
		{
			last = (char *)s;
		}
		if (*s == '\0')
		{
			return (char *)last;
		}
		s++;
	}
}
